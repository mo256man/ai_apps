class Color:
    """ST7789用BGR565カラー定義（framebufはBGR順で格納）"""
    BLACK = 0x0000
    WHITE = 0xFFFF
    RED = 0x001F
    GREEN = 0x07E0
    BLUE = 0xF800
    CYAN = 0xFFE0
    MAGENTA = 0xF81F
    YELLOW = 0x07FF
